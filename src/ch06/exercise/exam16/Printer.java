package ch06.exercise.exam16;

public class Printer {
	void println(int param) {
		System.out.println(param);
	}
	
	void println(boolean param) {
		System.out.println(param);
	}
	
	void println(double param) {
		System.out.println(param);
	}
	
	void println(String param) {
		System.out.println(param);
	}
}